class CarStats
	def initialize(c,m,t,a,mpg)
		@company = c
		@model = m
		@topspeed = t
		@acceleration = a
		@mpg = mpg
	end

	def company
		return @company
	end

	def model
		return @model
	end

	def topspeed
		return @topspeed
	end

	def acceleration
		return @acceleration
	end

	def mpg
		return @mpg
	end

	def breakstrength
		return @breakstrength
	end
	
end


